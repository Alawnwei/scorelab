def get_winrate(team_name: str) -> tuple:
    """
    获取球队赢盘率
    返回: (win_rate%, reliability)
    reliability: '高' | '中' | '低'
    如果找不到返回 (50.0, '低')
    """
    # 直接查找
    if team_name in WINRATE_DATABASE:
        wr, rel = WINRATE_DATABASE[team_name]
        return (float(wr), rel)

    # 别名匹配
    aliases = {
        "沙特": "沙特阿拉伯",
        "国米": "国际米兰",
        "尤文": "尤文图斯",
        "巴萨": "巴塞罗那",
        "拜仁": "拜仁慕尼黑",
        "巴黎": "巴黎圣日耳曼",
        "皇马": "皇马",
        "曼城": "曼城",
        "枪手": "阿森纳",
        "红军": "利物浦",
        "红魔": "曼联",
        "蓝军": "切尔西",
        "热刺": "热刺",
        "多特": "多特蒙德",
        "药厂": "勒沃库森",
    }
    if team_name in aliases:
        real_name = aliases[team_name]
        if real_name in WINRATE_DATABASE:
            wr, rel = WINRATE_DATABASE[real_name]
            return (float(wr), rel)

    return (50.0, "低")


def list_all_teams_with_winrate() -> list:
    """列出所有有赢盘率数据的球队"""
    return sorted(WINRATE_DATABASE.keys())

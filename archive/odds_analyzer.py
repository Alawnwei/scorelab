#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
赔率分析工具 v1.0
核心逻辑：找市场定价错误，而不是预测谁赢
"""
import sys

def p(text):
    try:
        print(text)
    except:
        print(str(text).encode('utf-8', errors='replace').decode('gbk', errors='replace'))

# ============================================================
# 核心概念1: 隐含概率与推水
# ============================================================
p("=" * 70)
p("核心概念1: 赔率到隐含概率到推水")
p("=" * 70)

odds_example = {"主胜": 2.10, "平局": 3.40, "客胜": 3.80}
p(f"示例赔率: {odds_example}")

p("\n步骤1: 原始隐含概率(含庄家抽水):")
total_imp = 0
for outcome, odd in odds_example.items():
    imp = 1/odd * 100
    total_imp += imp
    p(f"  {outcome}: 1/{odd} = {imp:.2f}%")
p(f"  总和: {total_imp:.2f}% (超过100%={total_imp-100:.2f}%=庄家抽水)")

p("\n步骤2: 推水后的真实概率:")
for outcome, odd in odds_example.items():
    fair = (1/odd) / total_imp * 100
    p(f"  {outcome}: {fair:.2f}%")
p(f"  总和: 100%")

p("\n步骤3: 如果模型估算主队真实胜率=55%:")
market = (1/odds_example["主胜"]) / total_imp * 100
model = 55.0
ev = (model / market - 1) * 100
p(f"  市场认为: {market:.1f}%")
p(f"  模型认为: {model:.1f}%")
p(f"  +EV价值: {ev:+.1f}%")
p(f"  结论: {'值得下注' if ev > 5 else '不值得下注'}")

# ============================================================
# 核心概念2: 三个盈利信号
# ============================================================
p("\n\n" + "=" * 70)
p("核心概念2: 三种盈利信号")
p("=" * 70)

p("""
信号1: 模型vs市场 -> +EV价值
  模型估算概率 > 赔率隐含概率 = +EV

信号2: 市场内部矛盾 -> 机构分歧
  竞彩vs Bet365 vs 1xbet 分歧 > 10% = 有人错了

信号3: 赔率变动方向 -> 聪明钱流向
  赔率从2.10降到1.85 = 聪明钱涌入
""")

# ============================================================
# 核心概念3: 下注纪律
# ============================================================
p("=" * 70)
p("核心概念3: 三条铁律")
p("=" * 70)

p("""
铁律1: 只在+EV>=5%时下注
铁律2: 凯利公式决定下注额
  下注比例 = (赔率 x 概率 - 1) / (赔率 - 1)
铁律3: 不追损,不贪心
  连错3场停,日盈利>20%停,日亏损>10%停
""")

# ============================================================
# 实战: 25场数据做+EV分析
# ============================================================
p("\n\n" + "=" * 70)
p("实战: 25场比赛的+EV分析")
p("=" * 70)

matches = [
    ("巴拿马vs英格兰","英格兰胜",1.20,85,1),("克罗地亚vs加纳","克罗地亚胜",1.55,70,1),
    ("哥伦比亚vs葡萄牙","平局",3.20,85,1),("民主刚果vs乌兹别克","民主刚果胜",2.10,85,1),
    ("约旦vs阿根廷","阿根廷胜",1.15,85,1),("阿尔及利亚vs奥地利","平局",3.30,85,1),
    ("赫尔辛基vs库奥皮奥","库奥皮奥不败",1.80,70,1),("玛丽港vs国际图尔库","国际图尔库胜",2.20,70,1),
    ("TPS图尔库vs雅罗","TPS胜",2.00,70,1),("赫尔辛基火花vs瓦萨","平局",3.00,70,1),
    ("AC奥卢vs拉赫蒂","AC奥卢胜",1.80,70,0),("山猫vs塞伊奈约基","山猫不败",1.70,70,1),
    ("南非vs加拿大","加拿大胜",2.10,70,1),("巴西vs日本","巴西胜",1.35,70,1),
    ("德国vs巴拉圭","德国胜",1.22,70,0),("荷兰vs摩洛哥","平局",3.40,70,1),
    ("墨西哥vs厄瓜多尔","平局",3.00,70,0),("法国vs瑞典","法国胜",1.35,85,1),
    ("科特迪瓦vs挪威","挪威胜",2.10,70,1),("西班牙vs奥地利","西班牙胜",1.35,85,1),
    ("葡萄牙vs克罗地亚","葡萄牙胜",1.85,70,1),("哥伦比亚vs加纳","哥伦比亚胜",1.42,85,1),
    ("天狼星vs米亚尔比","天狼星不败",1.70,70,1),("澳大利亚vs埃及","平局(临场)",2.88,70,1),
    ("阿根廷vs佛得角","阿根廷胜",1.15,85,0),
]

p(f"{'比赛':<20} {'方向':<16} {'赔率':<8} {'模型概率':<8} {'MIP':<8} {'+EV%':<8} {'结果':<4}")
p("-"*80)

all_bet=0; all_ret=0; ev_bet=0; ev_ret=0; ev_cnt=0; ev_win=0
for name, direction, odds, mp, win in matches:
    mip = (1/odds) / 1.05 * 100
    ev_pct = (mp / mip - 1) * 100
    result = "W" if win else "L"
    p(f"{name:<20} {direction:<16} {odds:<8.2f} {mp:<7.0f}% {mip:<6.1f}% {ev_pct:<+6.1f}%  {result}")
    all_bet+=100
    if win: all_ret+=100*odds
    if ev_pct>=5:
        ev_bet+=100; ev_cnt+=1
        if win: ev_ret+=100*odds; ev_win+=1

p(f"\n策略A: 全部下注 -> ROI={(all_ret-all_bet)/all_bet*100:.1f}%")
p(f"策略B: +EV>=5% ({ev_cnt}场, 命中{ev_win}) -> ROI={(ev_ret-ev_bet)/ev_bet*100:.1f}%")

# CLV示例
p("\n\n" + "=" * 70)
p("附录: CLV(Closing Line Value)")
p("=" * 70)

p("""
CLV = (你下注的赔率 - 最终赔率) / 最终赔率

正CLV = 你比市场聪明 -> 长期必然盈利
职业玩家目标: 长期CLV > 0%

建议: 从今天起记录每场下注的CLV
如果CLV持续为正但单场亏损 -> 只是随机波动
如果CLV为负 -> 你的判断方向错了,需要调整
""")

p("\n赔率分析工具已就绪。运行命令: python skill/odds_analyzer.py")
